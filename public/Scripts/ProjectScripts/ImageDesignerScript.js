document.querySelector("#DesignerPic").addEventListener('mouseenter' , (e)=> {
    document.querySelector("#DesignerPic").style.width = "160px" ;
    document.querySelector("#DesignerPic").style.height = "160px" ;
})


document.querySelector("#DesignerPic").addEventListener('mouseleave' , (e)=> {
    document.querySelector("#DesignerPic").style.width = "150px" ;
    document.querySelector("#DesignerPic").style.height = "150px" ;
})